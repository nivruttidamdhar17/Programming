class SwitchDemo
{
	public static void main(String[] args)
	{
		char ch='C';
		switch(ch)
		{
			case A:
				System.out.println("character A");
				break;
			case B:
				System.out.println("chatacter B");
				break;
			case C:
				System.out.println("character C");
				break;
			case D:
				System.out.println("character D");
				break;
			default :
				System.out.println("Default");
				break;
		}
	}
}
